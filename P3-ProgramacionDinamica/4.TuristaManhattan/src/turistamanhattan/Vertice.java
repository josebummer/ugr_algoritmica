package turistamanhattan;

/**
 *
 * @author Alex
 */
public class Vertice {
    private int i;
    private int j;
    private int valor;

    public Vertice(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
}
